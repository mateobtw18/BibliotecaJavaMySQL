    private void actualizarTabla(){
        String consulta = "select * from autor";
        try {
            conet = con1.conexion();
            st = conet.createStatement();
            rs = st.executeQuery(consulta);
            
            Object [] autor = new Object[3];
            tabla = (DefaultTableModel) this.jTlistaAutores.getModel();
            while(rs.next()){
                autor [0] = rs.getInt("idAutor");
                autor [1] = rs.getString("Nombre");
                autor [2] = rs.getString("FechaNacimiento");
                
                tabla.addRow(autor);
            }
            this.jTlistaAutores.setModel(tabla);
        } 
        catch (Exception e) {
            
        }
    }
        
    private void limpiarTabla(){
        for(int i = 0; i <= this.jTlistaAutores.getRowCount(); i++){
            tabla.removeRow(i);
            i--;
        }
    }
        
