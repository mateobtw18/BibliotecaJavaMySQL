    private void jBinsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBinsertActionPerformed
        int idAutor = Integer.parseInt(this.jTFidAutor.getText());
        String nombreAutor = this.jTFnombreAutor.getText();
        String fechaNacimientoAutor = this.jTFfechaNacimientoAutor.getText();
        
        try {
            if(nombreAutor.equals("") || fechaNacimientoAutor.equals("")){
                JOptionPane.showMessageDialog(null, "Faltan datos");
            }
            else{
                String consulta = "insert into autor(idAutor, Nombre, FechaNacimiento) values (" + idAutor + ",'" + nombreAutor + "','" + fechaNacimientoAutor + "')";
                conet = con1.conexion();
                st = conet.createStatement();
                st.executeUpdate(consulta);
                this.jTFidAutor.setEditable(false);
                JOptionPane.showMessageDialog(null, "Los datos han sido agregados");
                limpiarTabla();
            }  
        } 
        catch (Exception e) {
            
        }
        actualizarTabla();
    }//GEN-LAST:event_jBinsertActionPerformed

