    private void jBregistrarLibroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBregistrarLibroActionPerformed
        String idEstudiante = this.jTFidEstudiante.getText();
        String nombreEstudiante = this.jTFnombreEstudiante.getText();
        String fechaNacimientoEstudiante = "";

        String titulo = this.jTFtituloLibro.getText();
        String stock = this.jTFstockLibro.getText();
        
        String fechaPrestamo = "";
        String fechaRetorno = "";
        String cantidad = this.jTFCantidad.getText();
        

        String fechaPublicacion = "";
        if (this.jDCfechaNacimientoEstudiante.getDate() == null) {
            JOptionPane.showMessageDialog(null, "Por favor, seleccione una fecha de nacimiento válida.", "Error", JOptionPane.ERROR_MESSAGE);
        } else if (this.jDCfechaPublicacionLibro.getDate() == null) {
            JOptionPane.showMessageDialog(null, "Por favor, seleccione una fecha de publicación válida.", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            fechaNacimientoEstudiante = this.formatoFecha.format(this.jDCfechaNacimientoEstudiante.getDate());
            fechaPublicacion = this.formatoFecha.format(this.jDCfechaPublicacionLibro.getDate());
            fechaPrestamo = this.formatoFecha.format(jDCfechaPrestamo.getDate());
            fechaRetorno = this.formatoFecha.format(jDCfechaRetorno.getDate());
        }

        if (idEstudiante.isEmpty() || nombreEstudiante.isEmpty() || titulo.isEmpty() || stock.isEmpty() ||
                this.jDCfechaNacimientoEstudiante.getDate() == null || this.jDCfechaPublicacionLibro.getDate() == null) {
            JOptionPane.showMessageDialog(null, "Por favor, corrige los campos antes de registrar.", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            if (this.consulta.registrarPrestamo(cn, nombreEstudiante, titulo, fechaPrestamo, fechaRetorno, cantidad, idEstudiante, fechaNacimientoEstudiante,fechaPublicacion,stock)) {
                this.limpiarCeldasRegistrar();
                this.jTFnombreEstudiante.requestFocus();
            } else {
                this.metodoTabla.actualizarTablaPrestamo(cn, tablaPrestamo);
                this.metodoTabla.actualizarTablaLibro(cn, tablaLibro);
                this.metodoTabla.actualizarTablaEstudiante(cn, tablaEstudiante);
                this.jTlistaPrestamos.setModel(tablaPrestamo);
                this.jTlistalibros.setModel(tablaLibro);
                this.jTlistaEstudiantes.setModel(tablaEstudiante);
                this.jTFidEstudiante.setEditable(false);
                this.jTFnombreEstudiante.setEditable(false);
                this.jDCfechaNacimientoEstudiante.setEnabled(false);
                this.jTFtituloLibro.setEditable(false);
                this.jDCfechaPublicacionLibro.setEnabled(false);
                this.jTFstockLibro.setEditable(false);
            }
        }
    }//GEN-LAST:event_jBregistrarLibroActionPerformed

